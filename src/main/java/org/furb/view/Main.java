    private void archiveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_archiveButtonActionPerformed
        try {
            String path = archivePath.getText();
            ArchiveReader archive = new ArchiveReader(path);
            archive.readArchiveLines();

            archiveFeedback.setText("O arquivo est√° bem formatado!");

            DefaultTableModel model = (DefaultTableModel) archiveResult.getModel();

            model.setRowCount(0);

            for (Tag tag : archive.getHtmlValidator().getFrequencyDesc()) {
                System.out.println(tag.getContentCleared() + " frequencia: " + tag.getFrequency());
                model.addRow(new Object[]{ tag.getContentCleared().replaceAll("[<>]", ""), tag.getFrequency() });
            }

        } catch (Exception ex) {
            archiveFeedback.setText(ex.getMessage());
        }
      
    }//GEN-LAST:event_archiveButtonActionPerformed

